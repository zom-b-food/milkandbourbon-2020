						<li data-thumb="/dist/img/critic2.jpg">
							<div class="content white-text">
								<span class="white-text">A relaxing and humorous read.</span>
								<p>Walking Through Brambles by G. W. Latimer was a relaxing escape, and so much
									funnier than I expected. From his observations about yellow cat and
									fatherhood to his day to day life at the library, his observations and dry
									sense of humor had me laughing several times. I can see how many might miss
									the humor, but it's there and so rewarding. This book reads more like a
									diary or journal than a novel, but I took to reading it at night before
									bed. The authors' voice is calming--a perfect end to a busy day in this
									noisy and often stressful world. </p>
								<div class="testimonial-meta">
									Teri Case,

								</div>
							</div>
						</li>
						<li data-thumb="/dist/img/critic3.jpg">
							<div class="content white-text">
								<span class="white-text">Unique Read</span>

								<p>This is definitely a different style and not for everyone- it's more like
									reading a beautiful journal someone has left behind. The descriptions are
									beautiful and exquisite. It's a place I'd want to visit and a life I'd
									enjoy living!
									I liked this book & I would read more from the writer in the future.</p>
								<div class="testimonial-meta">
									Nola Lace,

								</div>
							</div>
