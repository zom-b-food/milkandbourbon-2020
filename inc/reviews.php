								<span class="white-text">A delicious read!</span>
								<p>This narrative reads more like light-hearted poetry. The graceful
									descriptions of space, time, light, dust, foliage, elements, pets and
									people were all masterfully written. I found myself wanting to be in the
									small Oregon coastal town, among its quirky inhabitants.</p>
