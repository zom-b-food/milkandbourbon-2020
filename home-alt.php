                                        we lived in a tiny "artsy" (meaning mouse and roach infested) studio in Gramercy
                                        Park. A couple of years later, our beautiful daughter was born and we moved to
                                        Texas where I went to work for its democratic governor. And suddenly I
                                        discovered that I was a closet socialist and then summarily "fired" when Bush
                                        came to town. I spent my early career looking for a career. Spent my mid-career
                                        trying to resuscitate my first career and soon thereafter settled into my
                                        current career that felt like coming to after falling down the balcony stairs. I
                                        love what I do and do it quite well. Initially, however, I wanted to write
                                        novels on a beach in Northern Africa or make cheese out of a wagon while
                                        traveling with gypsies, but I was pressed into a real job due to the above
                                        mentioned child and a delinquent student loan.</p>

                                    <p>I've always believed that life is a journey as depicted by the tiny rune tattooed
                                        on
                                        my hand. And so I aim to journey on without the
                                        pathos of a martyr looking over his shoulder at an image blurred by age and
                                        remorse.
                                        I try to guide my life by that tiny voice within me struggling each day to sever
                                        myself from ego. I hope to
                                        obtain someday the wise dignity of an artist or a scholar; always pressing
                                        forward
